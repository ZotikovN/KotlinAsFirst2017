/**
 * Сложная
 *
 * Найти n-ю цифру последовательности из квадратов целых чисел:
 * 149162536496481100121144...
 * Например, 2-я цифра равна 4, 7-я 5, 12-я 6.
 */
fun squareSequenceDigit(n: Int): Int {
    var n1: Int=1
    var no: Int=1
    var k: Int=0
    var m: Int=0
    var count: Int=0
    var ncount: Int=0
    var num: Int=0
    var difcount: Int=0
    when {
        n==1 -> return 1
        else -> {
            while (count<n) {
                k=no*no
                m=k
                while (m>0) {
                    m/=10
                    ncount+=1
                }
                count=count+ncount
                ncount=0
                no+=1
            }
            when {
                count==n -> num==k%10
                else -> {
                    difcount=count-n
                    do {
                        difcount-=1
                        k/=10
                    } while (difcount>0)
                }
            }
            return num
        }
    }
}

